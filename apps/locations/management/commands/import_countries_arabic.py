class Command(BaseCommand):
    help = 'Update countries with Arabic names'

    def handle(self, *args, **opts):
        updated = 0
        not_found = []

        countries = Country.objects.exclude(iso2='')
        total = countries.count()
        
        self.stdout.write(f'Updating {total} countries...')

        for country in countries:
            arabic_name = ARABIC_COUNTRY_NAMES.get(country.iso2.upper())
            if arabic_name:
                country.name_ar = arabic_name
                country.save(update_fields=['name_ar'])
                updated += 1
            else:
                not_found.append(country.iso2)
        
        self.stdout.write(self.style.SUCCESS(f'\n✅ {updated} countries updated with Arabic names'))
        if not_found:
            self.stdout.write(self.style.WARNING(f'⚠️  {len(not_found)} countries without Arabic name: {not_found}'))
